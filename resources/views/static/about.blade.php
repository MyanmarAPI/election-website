 			<p>Working with the <a href="http://uecmyanmar.org" target="_blank">Union Election Commission (UEC)</a>, we are compiling a public database of information about the 6000+ candidates for national and state/region parliamentary seats, the 93 political parties contesting the elections, and answers to voters’ frequently asked questions about the functions of government institutions, electoral processes, and how to vote.</p>
			
			<p>The database will be available to the public through the MaePaySoh Application Programming Interface (API).</p>
 
